buahan = ["apel","bengkoang","ceri"]

#looping, mengulang buah didalam list buahan
for buah in buahan:
    #melakukan transformasi dengan mencetak dengan title case
    print("Buah {} adalah buah yang enak.".format(buah.title()))

#Mencetak  diluar loop.
print("Semua buah-buahan adalah enak, karunia Tuhan.")

