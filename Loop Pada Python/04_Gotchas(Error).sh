1. Lupa mengidentasi menggunakan tombol TAB untuk operasi di dalam Loop
Contoh:
for i in items:
print(i) #lupa menekan TAB dan tidak ada identasi 

2. Melakukan identasi menggunakan tombol TAB ketika tidak diperlukan
Contoh:
pesan = "Selamat Datang"
    print(pesan) #tidak perlu identasi, tapi melakukan identasi

3. Melakukan Identasi untuk operasi diluar Loop
Contoh:
pesan = "Looping selesai"
for i in items:
    print(i)
    print(pesan) #Melakukan identasi untuk operasi yang seharusnya di luar Loop

4. Lupa memberikan titik dua (colon (:)) untuk pernyataan For..
Contoh:
for i in items #lupa memberikan tanda titik dua (:)
    print(i)
    